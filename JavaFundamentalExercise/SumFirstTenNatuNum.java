package JavaFundamentalExercise;

public class SumFirstTenNatuNum {
    public static void main(String[] args) {
        
        int result = 0;

        for (int i = 1; i < 11; i++) {
            result += i;
            System.out.println(i+" Sum First Ten Natural Num : " + result);
        }

    }
}
