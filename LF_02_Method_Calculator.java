public class LF_02_Method_Calculator {

    // static method
    static int add(int num1, int num2) {
        return num1 + num2;
    }

    static int sub(int num1, int num2) {
        return num1 - num2;
    }

    // instant method
    int divide(int num1, int num2) {
        return num1 / num2;
    }
}